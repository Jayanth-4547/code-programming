import java.util.*;
class M11{
  public static void main(String[] args) {
    int a[]={1,2,3,4};
    String b[]={"apple","ball","cat","dog"};
    char c[]={'a','b','c','d'};
    System.out.println("");
    for(int i:a)
      System.out.println(i);
    System.out.println("");
    for(String j:b)
      System.out.println(j);
    System.out.println("");
    for(char k:c)
      System.out.println(k);
  }
}